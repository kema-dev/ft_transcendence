<script setup lang="ts">
import { onMounted, inject } from "vue";
import ScoreItem from "../components/ScoreItem.vue";
import MatchItem from "@/components/MatchItem.vue";
let define = inject("colors");
let me = inject("user")!;
let socket = inject("socket")!;

var ProgressBar = require("progressbar.js");

let user = {
	name: "zeus",
	level: "1000",
	avatar: require("@/assets/avatars/(2).jpg"),
	friends: ["Jane", "John", "Jacksdfgtertwdsfadfsafdertert"],
	status: "offline",
	rank: "1st",
	ratiov: "10",
	ratiod: "5",
	history: [
		{
			adversary: "John",
			points1: 10,
			points2: 5,
		},
		{
			adversary: "Jacksdfgtertwdsfadfsafdertert",
			points1: 7,
			points2: 5,
		},
		{
			adversary: "John",
			points1: 3,
			points2: 5,
		},
	],
};
onMounted(() => {
	let input = document.querySelector("#none");
	input?.addEventListener("change", () => {
		const reader = new FileReader();
		reader.addEventListener("load", () => {
			let image = reader.result;
			document.querySelector("#img").src = `${image}`;
			socket.emit("changeAvatar", {
				login: me.value.login,
				avatar: `${image}`,
			});
		});
		reader.readAsDataURL(input.files[0]);
	});
	var bar = new ProgressBar.Circle("#bar", {
		color: define.color2,
		strokeWidth: 4,
		trailWidth: 0,
		easing: "easeInOut",
		duration: 1400,
	});
	bar.animate(1 - me?.value?.ratio);
});
function change_avatar() {
	let input = document.querySelector("#none");
	input.click();
}
function avatar() {
	if (me.value) {
		return window.btoa(me.value.avatar);
	}
}
</script>

